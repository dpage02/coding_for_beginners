# storing values
a = 8
b = 4

print(f"Assign Values:\t a = {a} \tb = {b}")

# addition
a += b
print(f'Add & Assign:\t a = {a} \t(8 + 4)')

# subtraction
a -= b
print(f'Subtract & Assign:\t a = {a} \t(12 - 4)')

# mulitplication
a *= b
print(f'Multiplication & Assign:\t a = {a} \t(8 X 4)')

# division and floor
a /= b
print(f'Divide & Assign:\t a = {a} \t(32 / 4)')
a %= b
print(f'Floor & Assign:\t a = {a} \t(8 % 4)')
