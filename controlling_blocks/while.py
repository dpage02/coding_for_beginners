# initialize value
i = 1
while i < 4:

    # dispaly the counter's value, increment value on each iteration 
    print('Outer Loop Iteration:', i)
    i += 1

    # initialize second counter, define inner loop 
    j = 1
    while j < 4:

        # display j counter, increment value on each iteration
        print('\tInner Loop Iteration')
        j += 1