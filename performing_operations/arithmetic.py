# storing values
a = 8
b = 2

# addition
print(f'Addition:\t {a} + {b} = {a+b}')

# subtration 
print(f'Subtraction:\t {a} - {b} = {a-b}')

# multiplication 
print(f'Multiplication:\t {a} x {b} = {a*b}')

# division, division with floor 
print(f'Divison:\t {a} / {b} = {a/b}')
print(f'Floor Division:\t {a} / {b} = {a//b}')

# remainder
print(f'Remainder:\t {a} % {b} = {a%b}')

# exponent
print(f'Exponent:\t {a}^{b} = {a**b}', sep='')
